package ifelseAndBasics;
public class MultipleOf5 {
    public static void main(String[] args) {
        // Write a Java program to check whether
        // the given integer is a multiple of 5

        int n = 25;

        if (n % 5 == 0) {
            System.out.println("Yes");
        } else {
            System.out.println("No");
        }
    }
}
